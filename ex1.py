# Considerando 2 números inteiros, faça a soma, subtração e multiplicação. Por fim, mostre os resultados.

num1 = int(input("informe um número inteiro: "))
num2 = int(input("informe outro número inteiro: "))

soma = num1 + num2
sub = num1 - num2
mult = num1 * num2

print("a soma de", num1, "e", num2, "é", soma)
print("a subtração de", num1, "e", num2, "é", sub)
print("a multiplicação de", num1, "e", num2, "é", mult)
