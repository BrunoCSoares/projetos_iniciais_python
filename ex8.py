# · Tendo três valores inteiros (representados pelas variáveis A, B e C), calcule e apresente como resultado final o valor da soma dos quadrados dos três valores lidos.

a = int(input("Informe um valor inteiro: "))
b = int(input("Informe outro valor inteiro: "))
c = int(input("Informe outro valor inteiro: "))

r = a**2 + b**2 + c**2

print(r)